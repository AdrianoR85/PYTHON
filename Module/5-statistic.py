import statistics

# Aplicar a média
print(statistics.mean([3,2,5,8,9]))

# Aplicar a mediana
print(statistics.median([3,2,3,7,9,10]))
print(statistics.median([3,2,3,9,10]))

# Aplicar a moda
print(statistics.mode([3,2,3,7,2,8,7,9,10,2]))

# Desvio padrão

print(statistics.stdev([3,2,4,1,2]))
print(statistics.stdev([1,1.5,2,2.5,3,3.5,4,4.5]))
print(statistics.stdev([3,2,3,9,10]))

