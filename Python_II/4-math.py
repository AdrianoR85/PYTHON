import math

print(math.pi) # PI

num = 10.4
print(math.ceil(num)) # arredonda para cima
print(math.floor(num)) # arredonda para baixo

num2 = 25
print(math.sqrt(num2)) # raíz quadrada

num3 = 7
print(math.factorial(num3)) # fatorial

print(math.pow(5, 2)) # potenciação

print(math.gcd(12, 4, 6)) #MDC